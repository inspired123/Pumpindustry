            <p>The affinity laws of centrifugal pump performance express the effect, in mathematical equations, on pump performance due to changes in certain application variables. The affinity law variables which affect pump performance are: 1. RPM revolutions per minute. 2. Impeller diameter.
      <OL>
        <LI>Changing the Pump Speed (RPM):
            <UL>
              <LI>When the impeller diameter of a centrifugal pump is held constant the effect of changing the speed (RPM) of the pump is in accordance with the following relationships:
              <LI>Capacity: Q1/Q2 = N1/N2 Head: H1/H2 = (N1/N2)2 BHP: BHP1/BHP2 = (N1/N2)3<BR>
        Where subscript number 1 indicates the initial conditions and subscript 2 at the new conditions., and:
              <LI>BHP = Brake Horsepower H = Head, Feet N = Pump Speed (RPM ) Q = Capacity (GPM )</LI>
            </UL>
            <P></P>
        </LI>
        <LI>Changing the speed (RPM) of a pump affects the flow, head and input brake horsepower of the pump in different proportions. Changing the speed affects the flow through the pump by a proportion equal to the increase or decrease in speed. The pump head is changed by the square of the proportion of speed change, while the brake horsepower is changed by the cube of the proportion of speed change.
            <P></P>
        </LI>
        <LI>Changing the Impeller Diameter: When the speed (RPM) of a centrifugal pump is held constant the effect of changing the impeller diameter (D) is as follows:
            <UL>
              <LI>Capacity: Q1/Q2 = D1/D2 Head: H1/H2 = (D1/D2)2 BHP: BHP1/BHP2 = (D1/D2)3 Where subset number 1 shows performance at the initial impeller diameter and subset number 2 shows performance at the new impeller diameter, and:
              <LI>Q = Capacity (GPM ) H = Head, Feet BHP = Brake Horsepower</LI>
            </UL>
            <P></P>
        </LI>
      </OL>
      <P>In other words, capacity varies in proportion to speed, head to the square of the speed and power consumed in proportion to the cube of the speed.
</p>          </div>
