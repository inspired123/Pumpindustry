       <div class="panel panel-default">
        <div class="panel-heading" id="headingFourteen" role="tab">
          <h4 class="panel-title"><a class="collapsed" role="button" data-toggle="collapse"  style="font-size: 21px;color: brown"data-parent="#accordion" 
          href="#collapseFourteen" aria-expanded="false" aria-controls="collapseFourteen">What is a self-priming pump and how does it work?<i class="pull-right fa fa-plus"></i></a></h4>
        </div>
        <div class="panel-collapse collapse" id="collapseFourteen" role="tabpanel" aria-labelledby="headingFourteen">
          <div class="panel-body">
            <p>Priming is the addition of liquid into the pump casing which aides in the evacuation of captured air via a vent while creating a liquid seal inside the casing.
<br>
<br>
A self-priming pump develops a vacuum sufficient enough for atmospheric pressure to force the liquid to flow through the suction pipe into the pump casing without priming the pump. A self-priming centrifugal pump is especially designed with a large chamber at its discharge side that acts both as an air separator that removes the air from the liquid, and a reservoir that holds residual liquid used for priming or re-priming the pump. The pump has to be primed during the initial start-up but re-priming is done automatically without outside attention. Although self-priming appears to be a desirable feature for centrifugal pumps the trade-off is that pump efficiency is slightly compromised due to certain design constraints. This type of pump is popular with intermittent service such as drainage, sewage, and similar applications, but is less popular in continuous service applications where optimum efficiency is desirable and re-priming is seldom needed due to continuous operation.</p>
      </div>
        </div>
      </div>
      
       <div class="panel panel-default">
        <div class="panel-heading" id="headingFifteen" role="tab">
          <h4 class="panel-title"><a class="collapsed" role="button" data-toggle="collapse"  style="font-size: 21px;color: brown"data-parent="#accordion" 
          href="#collapseFifteen" aria-expanded="false" aria-controls="collapseFifteen">Why is specific speed important?<i class="pull-right fa fa-plus"></i></a></h4>
        </div>
        <div class="panel-collapse collapse" id="collapseFifteen" role="tabpanel" aria-labelledby="headingFifteen">
          <div class="panel-body">
            <p>Specific speed is defined as the speed in revolutions per minute at which an impeller would operate if reduced proportionately in size so as to deliver a unit of capacity against a unit of total head. The performance of a pump can be determined by stating its speed, the head it generates and the rate of discharge. Yet, when comparing one pump to another, it is more useful to use one term which will provide a general description of the pump's performance characteristics.
<br>
<br>
Different types of impellers and flow patterns can best be compared by means of the specific speed. A pump can be classified as axial flow, mixed flow, or radial flow depending where the impeller discharges the liquid. An axial flow pump discharges the liquid in the axial direction compared to the pump shaft centerline, a radial flow pump discharges the liquid in the radial direction and a mixed flow pump is one that is a combination between a radial and an axial flow pump design.
<br>
<br>

Pumps have to be selected with the right specific speed impellers in order to operate satisfactorily in the desired application. High specific speed impellers (as fitted in axial flow and mixed flow pumps) are for high flow and lower head applications (such as ditch drainage, water supply operations, lift stations, flood control etc.) where huge amounts of water need to be moved quickly. Low specific speed impellers (such as those installed in radial flow pumps) produce higher heads (pressures) at moderate flow rates, typically this type of pump is used to move liquids to much higher elevation levels (e.g. water well pumps, process pumps, boiler feed pumps etc).</p>
      </div>
        </div>
      </div>
      
       <div class="panel panel-default">
        <div class="panel-heading" id="headingSixteen" role="tab">
          <h4 class="panel-title"><a class="collapsed" role="button" data-toggle="collapse"  style="font-size: 21px;color: brown"data-parent="#accordion" 
          href="#collapseSixteen" aria-expanded="false" aria-controls="collapseSixteen">What is Best Efficiency Point?<i class="pull-right fa fa-plus"></i></a></h4>
        </div>
        <div class="panel-collapse collapse" id="collapseSixteen" role="tabpanel" aria-labelledby="headingSixteen">
          <div class="panel-body">
            <p>Best Efficiency Point (BEP) is the point at which the impeller diameter provides the highest efficiency. BEP is an important parameter in that many parametric calculations are considered when calculating BEP, such as size, , specific speed, viscosity correction, and headrise to shut-off. Professional users prefer that pumps operate within 80% to 110% of BEP for optimum performance.
<br>
<br>
As you move away from the BEP, the shaft will deflect and the pump will experience some vibration. Check with the pump manufacturer to determine safe deviation levels from the BEP. Operating your pump away from BEP not only reduces efficiency but it will increase maintenance costs and may damage the pump. You can change the operating point by installing control valves, changing the impeller diameter, installing recirculation lines or selecting a different size of pump.
<br>
<br>

Sometimes "safety" factors can mean that the system is designed with a pump which is too large. Such a pump will operate to the left of the BEP as shown on the manufacturer's curve. Thus, the radial loads generated by the impeller and supported by the bearings will be greater (at BEP, most pumps are designed to generate no hydraulically generated radial loads). So when you are designing a system try not to use any safety factors, get as much information about the required flows and the "system curve" (the profile of the resistance of the system to increasing flows) to allow the pump maker to select exactly the right pump for your operation.</p>
      </div>
        </div>
      </div>
      
       <div class="panel panel-default">
        <div class="panel-heading" id="headingSeventeen" role="tab">
          <h4 class="panel-title"><a class="collapsed" role="button" data-toggle="collapse"  style="font-size: 21px;color: brown"data-parent="#accordion" 
          href="#collapseSeventeen" aria-expanded="false" aria-controls="collapseSeventeen">What is cavitation?<i class="pull-right fa fa-plus"></i></a></h4>
        </div>
        <div class="panel-collapse collapse" id="collapseSeventeen" role="tabpanel" aria-labelledby="headingSeventeen">
          <div class="panel-body">
            <p>Cavitation occurs when the pressure of the liquid in the suction line of the pump falls towards the "vapor pressure" (the pressure at which the liquid will turn from a liquid into a gas, or vaporize/boil). This occurs when the liquid is drawn into the "eye" of the pump impeller. If there is not enough suction head (head of liquid at the suction side of the pump) to allow the liquid to enter the pump without it falling to its vapor pressure, then vapor bubbles will appear. As the pump adds pressure energy to the fluid these bubbles rapidly and violently collapse back into a liquid state. These collapsing bubble carry a lot of energy and they can cause significant damage as they implode. This process of vapor bubble formation and implosion is called "cavitation".
<br>
<br>
Different fluids cavitate under different conditions of pressure and temperature and different pumps require different suction heads (known as "Net Positive Suction Head Required [NPSHr]) to operate without damaging cavitation.
<br>
<br>

Cavitation can accelerate the corrosion, pitting and erosion of pumps and pump parts, shortening the life of the pump. Prior to start up, priming will remove the air, ensuring that the available NPSH in the suction line is greater than that required by the pump (NPSHa > NPSHr).</p>
      </div>
        </div>
      </div>
      
