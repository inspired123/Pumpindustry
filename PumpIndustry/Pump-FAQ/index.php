        <div class="panel-collapse collapse" id="collapseTwelve" role="tabpanel" aria-labelledby="headingTwelve">
          <div class="panel-body">
            <p> American National Standards Institute. Per the ANSI website: "the American National Standards Institute (www.ansi.org) has served in its capacity as administrator and coordinator of the United States private sector voluntary standardization system for more than 80 years. Founded in 1918 by five engineering societies and three government agencies, the Institute remains a private, nonprofit membership organization supported by a diverse constituency of private and public sector organizations. Throughout its history, the ANSI Federation has maintained as its primary goal the enhancement of global competitiveness of U.S. business and the American quality of life by promoting and facilitating voluntary consensus standards and conformity assessment systems and promoting their integrity. The Institute represents the interests of its nearly 1,000 company, organization, government agency, institutional and international members through its office in New York City, and its headquarters in Washington, D.C.<br>
