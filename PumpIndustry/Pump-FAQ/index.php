      <br>
      <br>
      Maintenance of the pump consists of periodically replacing the wearing parts such as sleeves, neck rings, packing material, lubricating mediums and others. Impellers and diffusers usually have a long life provided the pumped liquid is not corrosive or harmful. Damage may be caused by a combination of factors - corrosion, erosion, electrolytic action, graphitization and corrosion fatigue of the shaft.
 </p>
      </div>
        </div>
      </div>
       <div class="panel panel-default">
        <div class="panel-heading" id="headingEleven" role="tab">
          <h4 class="panel-title"><a class="collapsed" role="button" data-toggle="collapse"  style="font-size: 21px;color: brown"data-parent="#accordion" 
          href="#collapseEleven" aria-expanded="false" aria-controls="collapseEleven">What is a process pump?<i class="pull-right fa fa-plus"></i></a></h4>
        </div>
        <div class="panel-collapse collapse" id="collapseEleven" role="tabpanel" aria-labelledby="headingEleven">
          <div class="panel-body">
            <p>As defined by Webster's Ninth Edition, process is "a series of actions or operations conducing to an end; especially a continuous operation or treatment especially in manufacturing." So a process pump would be a pump used in the manufacturing of an end product. ANSI B73.1 Process Pumps are widely used in the chemical, pulp & paper, pharmaceutical, petroleum and general industries. These pumps are specifically designed to stand up to the heavy duty usage demands of process systems and operations. In addition, they can be fitted with a huge range of options to suit the varying demands of process operators.</p>
      </div>
        </div>
      </div>
      
       <div class="panel panel-default">
        <div class="panel-heading" id="headingTwelve" role="tab">
          <h4 class="panel-title"><a class="collapsed" role="button" data-toggle="collapse"  style="font-size: 21px;color: brown"data-parent="#accordion" 
          href="#collapseTwelve" aria-expanded="false" aria-controls="collapseTwelve">What does ANSI stand for?<i class="pull-right fa fa-plus"></i></a></h4>
