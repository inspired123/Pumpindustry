ANSI does not itself develop American National Standards (ANS); rather it facilitates development by establishing consensus among qualified groups. The Institute ensures that its guiding principles -- consensus, due process and openness -- are followed by the more than 175 distinct entities currently accredited under one of the Federation's three methods of accreditation (organization, committee or canvass). In 1999 alone the number of American National Standards increased by nearly 5.5% to a new total of 14,650 approved ANS. ANSI-accredited developers are committed to supporting the development of national and, in many cases international standards, addressing the critical trends of technological innovation, marketplace globalization and regulatory reform."<br>
