        </div>
        <div class="panel-collapse collapse" id="collapseThirteen" role="tabpanel" aria-labelledby="headingThirteen">
          <div class="panel-body">
            <p>In using an ANSI B73.1 pump, a user is assured that they have purchased a pump that was designed to meet the criteria set forth by both pump users and manufactures for the chemical industry. Not only does an ANSI B73.1 standard ensure dimensional interchangeability, but it also ensures that the customer is using a pump designed for easy installation and increased MTBF (mean time before failure).</p>
